<h2><b id="lblid"> History</b></h2>
 <p style="color:#ff3377";><em>Vivekanand Education Society</em> was founded in 1959 by Shri. Hashu Advani, 
 along with ten other members, who shared the dream of providing qualitative education to the youth of our country. 
 In the beginning, <em>Vivekanand Education Society</em> had a very modest launch, with just 256 students and six classrooms, 
 in the humble barracks of Chembur Camp. But today, it proudly boast of having 3, 75, 000 sq. ft. land, housing 12 buildings and 28 Institutions, 
 ranging from a crèche to Ph.D. Centers. It has over 2000 teaching and non-teaching staff, and more than 18,000 students who pass through its hallowed 
 portals each year.</p>
 <p>&nbsp;</p>
 <p style="color:#ff3377";>Vivekanand Education Society’s Institute of Technology (VESIT) was established in 1984, with the aim of providing professional 
 education in the field of Engineering. This institute is affiliated to the University of Mumbai and follows the rules and regulations laid down by government, 
 AICTE, and University for admission; 51% reserved for Sindhi Linguistic&nbsp;minority and 49% through CAP test. The management quota has been surrendered to 
 DTE to make admission centralized.The institute has a total of 189 faculties, of which 21 are Ph.D. holders and 29 faculty members are pursuing their Ph.D.</p>
