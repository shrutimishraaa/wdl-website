                    <p>The Department of Information Technology was established in the year 1999 with an intake of 40 students and increased to 60 in the next Academic year. Post Graduate course was inducted in the year 2002 with an intake of 18 students.</p>

<p>Presently the intake of students for the U.G. course is 120 in the morning shift.</p>

<p>With the fast-changing world, certain changes are essential, considering these aspects the Department of Information Technology aims at training the students to adapt themselves to the fast-changing technologies. Our Department strives for enhancing critical thinking, the ability to change information into knowledge, and to increase the power of information analysis.</p>

<p>The department has flourished due to the collective efforts of the management, skilled staff, and an efficient student base. We, at the department level, are making an earnest effort to make IT curriculum a perfect mix of theory, practice, and implementation.</p>
<p><strong> Vision : </strong><br>
To create a high quality academic and research environment, which empowers faculty and students to attain the highest levels of excellence as professionals in the field of Information Technology, in turn leading to the creation of leaders and trend-setters for the next generation IT Industry.</p>
<p><strong> Mission :</strong></p>
<ol>
<li> To Provide quality education that offers opportunities for lifelong learning at multiple academic and professional levels.</li>
<li>To Promote and support faculty involvement in all aspects of IT research and development.</li>
